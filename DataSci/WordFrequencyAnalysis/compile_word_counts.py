def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("-o", type=str, help="output json file", required=True)
    parser.add_argument("-d", type=valid_csv, help="input csv file", required=True)
    args = parser.parse_args()

    if (not os.path.exists(args.o)):
        os.mkdir(args.o)
    
    input = pd.read_csv(args.d)

    ponies = ['Twilight Sparkle', 'Applejack', 'Rarity', 'Pinkie Pie', 'Rainbow Dash', 'Fluttershy']
    result = {}
    for pony in ponies:
        word_counts = compute_word_counts(input, pony)
        dict = {pony.lower(): word_counts}
        result.update(dict)

    with open(args.o, 'w') as f:
        json.dump(result, f, indent=4)
        

def compute_word_counts(input, pony):
    word_counts = {}
    for ind in input.index:
        if (input['pony'][ind] == pony):
            for word in input['dialog'][ind].split():
                word = word.lower()
                word = word.strip("'[]()-.,!?:;#&")
                if (word not in stop_words):
                    if (word in word_counts):
                        word_counts[word] += 1
                    else:
                        word_counts[word] = 1
    
    for key in list(word_counts):
        if (word_counts[key] < 5):
            del word_counts[key]

    sorted_dict = dict(sorted(word_counts.items(), key=lambda item: item[1], reverse=True))
                
    return sorted_dict
    

def valid_json(path):
    try:
        json.load(open(path))
        return path
    except:
        raise argparse.ArgumentTypeError("Invalid json file")

def valid_csv(path):
    try:
        pd.read_csv(path)
        return path
    except:
        raise argparse.ArgumentTypeError("Invalid csv file")

if __name__ == '__main__':
    main()